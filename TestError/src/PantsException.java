
public class PantsException extends RuntimeException {

	public PantsException() {
		super();
		
	}

	public PantsException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public PantsException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public PantsException(String message) {
		super(message);
		
	}

	public PantsException(Throwable cause) {
		super(cause);
		
	}
	
	
}
