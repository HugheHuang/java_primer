
public class ShoeException extends Exception {

	public ShoeException() {
		super();
		
	}

	public ShoeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public ShoeException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public ShoeException(String message) {
		super(message);
		
	}

	public ShoeException(Throwable cause) {
		super(cause);
		
	}

}
